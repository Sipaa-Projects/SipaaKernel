#include <sterm.h>
#include <sipaa/memory.h>

unsigned char deffont[];

struct terminal terminal_info;

void sterm_init(
    uint32_t *fbptr, int fbwidth, int fbheight,
    uint32_t defbg, uint32_t deffg,
    void *font, int fontwidth, int fontheight, int fontspacing)
{
    terminal_info.framebuffer_address = fbptr;
    terminal_info.width = fbwidth;
    terminal_info.height = fbheight;
    terminal_info.cursor_x = 0;
    terminal_info.cursor_y = 0;

    if (defbg >= 0)
    {
        terminal_info.defbg = defbg;
    }
    else
    {
        terminal_info.defbg = 0x0;
    }

    if (deffg >= 0)
    {
        terminal_info.deffg = deffg;
    }
    else
    {
        terminal_info.deffg = 0xFFFFFF;
    }

    terminal_info.bg = terminal_info.defbg;
    terminal_info.fg = terminal_info.deffg;

    if (font)
    {
        terminal_info.font = font;
        terminal_info.font_width = fontwidth;
        terminal_info.font_height = fontheight;
        terminal_info.font_spacing = fontspacing;
    }
    else
    {
        terminal_info.font = (void *)&deffont;
        terminal_info.font_width = 8;
        terminal_info.font_height = 16;
        terminal_info.font_spacing = 0;
    }

    sterm_clear();
}

void sterm_put_pixel(int x, int y, uint32_t color)
{
    if (x >= 0 && x < terminal_info.width && y >= 0 && y < terminal_info.height)
    {
        terminal_info.framebuffer_address[y * terminal_info.width + x] = color;
    }
}

uint32_t sterm_get_pixel(int x, int y)
{
    if (x >= 0 && x < terminal_info.width && y >= 0 && y < terminal_info.height)
    {
        return terminal_info.framebuffer_address[y * terminal_info.width + x];
    }
}

void sterm_set_bg(uint32_t bgc)
{
    if (bgc == -1)
    {
        terminal_info.bg = terminal_info.defbg;
    }

    if (bgc >= 0)
        terminal_info.bg = bgc;
}

void sterm_set_fg(uint32_t fgc)
{
    if (fgc == -1)
        terminal_info.fg = terminal_info.deffg;

    if (fgc >= 0)
        terminal_info.fg = fgc;
}

void sterm_clear()
{
    // Clear and reset cursor position
    SetMemory(terminal_info.framebuffer_address, terminal_info.bg,
           terminal_info.width * terminal_info.height * 4);

    terminal_info.cursor_x = 0;
    terminal_info.cursor_y = 0;
}

void sterm_scroll_up()
{
    for (int y = 0; y < terminal_info.height - 16; y++)
    {
        for (int x = 0; x < terminal_info.width; x++)
        {
            uint32_t color = sterm_get_pixel(x, y + 16);
            sterm_put_pixel(x, y, color);
        }
    }

    for (int i = 0; i < 0 + terminal_info.width; i++)
    {
        for (int j = terminal_info.height - 15; j < terminal_info.height - 16 + 16; j++)
        {
            sterm_put_pixel(i, j, terminal_info.bg);
        }
    }
}
void sterm_put_char(char character)
{
    if (character == '\n')
    {
        terminal_info.cursor_x = 0;
        terminal_info.cursor_y += 16;

        return;
    }

    if (terminal_info.cursor_x >= terminal_info.width)
    {
        terminal_info.cursor_x = 0;
        terminal_info.cursor_y += 16;
    }

    if (terminal_info.cursor_y >= terminal_info.height)
    {
        // TODO: scrolling
        terminal_info.cursor_x = 0;
        terminal_info.cursor_y = 0;
    }

    if (character == '\t')
    {
        terminal_info.cursor_x = 8 * 4;

        return;
    }

    const uint8_t *character_bitmap = &terminal_info.font[character * terminal_info.font_height];

    for (int i = 0; i < terminal_info.font_height; i++)
    {
        uint8_t line = character_bitmap[i];

        for (int j = 0; j < terminal_info.font_width; j++)
        {
            sterm_put_pixel(terminal_info.cursor_x + j, terminal_info.cursor_y + i, terminal_info.bg);

            if (line & (1 << (7 - j)))
            {
                sterm_put_pixel(terminal_info.cursor_x + j, terminal_info.cursor_y + i, terminal_info.fg);
            }
        }
    }

    terminal_info.cursor_x += terminal_info.font_width + terminal_info.font_spacing;
}

void sterm_put_string(const char *string)
{
    while (*string)
    {
        sterm_put_char(*string++);
    }
}

